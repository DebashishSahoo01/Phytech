#include<stdio.h>

int main(){
    int a,b;
    scanf("%d",&a);
    scanf("%d",&b);
    printf("greater then :%d\n",a>b);
    printf("not equal to:%d\n",a!=b);
    printf("equal to:%d\n",a==b);
    printf("less then:%d\n",a<b);
    printf("greater then equal to:%d\n",a>=b);
    printf("less then equal to:%d\n",a<=b);
}